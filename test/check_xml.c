/*--------------------------------------------------------------------------------------------------------------------*/

#include <stdio.h>

#include "../src/nyx_node.h"

/*--------------------------------------------------------------------------------------------------------------------*/

int main(int argc, char **argv)
{
    if(argc != 2)
    {
        fprintf(stderr, "Syntax: %s <xml string>\n", argv[0]);

        return 1;
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    nyx_memory_initialize();

    /*----------------------------------------------------------------------------------------------------------------*/

    nyx_xmldoc_t *xmldoc = nyx_xmldoc_parse(argv[1]);

    if(xmldoc == NULL)
    {
        goto _err;
    }

    str_t string = nyx_xmldoc_to_string(xmldoc);

    printf("%s\n", string);

    nyx_memory_free(string);
    nyx_xmldoc_free(xmldoc);

    /*----------------------------------------------------------------------------------------------------------------*/

    nyx_memory_finalize();

    printf("[SUCCESS]\n\n");

    return 0;

_err:
    nyx_memory_finalize();

    printf("[ERROR]\n\n");

    return 1;
}

