import typing

########################################################################################################################

from .. import bind
from .. import obj

########################################################################################################################

class NyxNull(obj.NyxObject):

    ####################################################################################################################

    def __init__(self, ptr = None):

        if ptr is None:

            ptr = bind.lib.nyx_null_new()

        elif bind.lib.nyx_object_get_type(ptr) != 0x65656500:

            raise TypeError('Not a pointer to a Nyx null object')

        super().__init__(ptr)

    ####################################################################################################################

    def get(self) -> typing.Any:

        return None

    ####################################################################################################################

    def set(self, value: typing.Any) -> bool:

        return False

########################################################################################################################

__all__ = ['NyxNull']

