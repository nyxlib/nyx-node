import ctypes

########################################################################################################################

from .. import bind
from .. import obj

########################################################################################################################

class NyxString(obj.NyxObject):

    ####################################################################################################################

    def __init__(self, ptr = None):

        if ptr is None:

            ptr = bind.lib.nyx_string_new()

        elif bind.lib.nyx_object_get_type(ptr) != bind.NYX_TYPE_STRING:

            raise TypeError('Not a pointer to a Nyx string object')

        super().__init__(ptr)

    ####################################################################################################################

    @property
    def value(self) -> str:

        return bind.lib.nyx_string_get(self.ptr).decode('utf-8')

    ####################################################################################################################

    @value.setter
    def value(self, value: str) -> bool:

        ################################################################################################################

        data = bind.lib.nyx_string_dup(bind.as_bytes(value, allow_none = False))

        ################################################################################################################

        return bool(bind.lib.nyx_string_set(self.ptr, ctypes.cast(data, bind.c_char_p), True))

########################################################################################################################

__all__ = ['NyxString']

