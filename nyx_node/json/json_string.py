import ctypes

########################################################################################################################

from .. import bind
from .. import obj

########################################################################################################################

class NyxString(obj.NyxObject):

    ####################################################################################################################

    def __init__(self, ptr = None):

        if ptr is None:

            ptr = bind.lib.nyx_string_new()

        elif bind.lib.nyx_object_get_type(ptr) != bind.NYX_TYPE_STRING:

            raise TypeError('Not a pointer to a Nyx string object')

        super().__init__(ptr)

    ####################################################################################################################

    def _get_buff(self) -> bytes:

        ################################################################################################################

        result_size = bind.c_size_t()
        result_buff = bind.c_void_p()

        bind.lib.nyx_string_get_buff(self.ptr, ctypes.byref(result_size), ctypes.byref(result_buff))

        ################################################################################################################

        return ctypes.string_at(result_buff.value, result_size.value)

    ####################################################################################################################

    @property
    def value(self) -> str:

        return self._get_buff().decode('utf-8')

    ####################################################################################################################

    @property
    def buff(self) -> bytes:

        return self._get_buff()#decode('utf-8')

    ####################################################################################################################

    @value.setter
    def value(self, value: str) -> bool:

        ################################################################################################################

        data = bind.as_bytes(value, allow_none = False)

        buff = ctypes.cast(bind.lib.nyx_string_ndup(data, len(data)), bind.c_char_p)

        ################################################################################################################

        return bool(bind.lib.nyx_string_set(
            self.ptr,
            buff,
            True
        ))

    ####################################################################################################################

    @buff.setter
    def buff(self, value: bytes) -> bool:

        ################################################################################################################

        data = bind.as_bytes(value, allow_none = False)

        buff = ctypes.cast(bind.lib.nyx_buffer_ndup(data, len(data)), bind.c_void_p)

        ################################################################################################################

        return bool(bind.lib.nyx_string_set_buff(
            self.ptr,
            len(data),
            buff,
            True
        ))

########################################################################################################################

__all__ = ['NyxString']

