########################################################################################################################

class NyxMessage(json_dict.NyxDict):

    ####################################################################################################################

    def __init__(self, device: str, message: str | None = None):

        super().__init__(bind.lib.nyx_message_new(
            bind.as_bytes(device, allow_none = False),
            bind.as_bytes(message)
        ))

