import typing

########################################################################################################################

def nyx_property(name: str, key: str, getter: typing.Callable[[typing.Any], typing.Any] | None = None, setter: typing.Callable[[typing.Any], typing.Any] | None = None):

    def decorate(cls: type) -> type:

        ################################################################################################################

        if name in cls.__dict__:

            raise AttributeError(f'Nyx property already exists: {name}')

        ################################################################################################################

        def get_value(self):

            value = self[key].get()

            if getter is not None:

                value = getter(value)

            return value

        ################################################################################################################

        def set_value(self, value) -> None:

            if setter is not None:

                value = setter(value)

            self[key].set(value)

        ################################################################################################################

        setattr(cls, name, property(get_value, set_value))

        ################################################################################################################

        return cls

    return decorate

########################################################################################################################

__all__ = [name for name in globals() if name.lower().startswith('nyx')]

