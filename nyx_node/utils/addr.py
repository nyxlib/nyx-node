from .. import ffi

########################################################################################################################

def nyx_generate_mac_addr(mac0: int, mac1: int, node_id: str) -> bytes:

    ####################################################################################################################

    node_id = ffi.as_bytes(node_id, allow_none = False)

    ####################################################################################################################

    result_mac = (ffi.c_uint8 * 6)()

    ffi.lib.nyx_generate_mac_addr(
        result_mac,
        mac0 & 0xFF,
        mac1 & 0xFF,
        node_id
    )

    return bytes(result_mac)

