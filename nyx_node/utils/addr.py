from .. import bind

########################################################################################################################

def nyx_generate_mac_addr(mac0: int, mac1: int, node_id: str) -> bytes:

    ####################################################################################################################

    node_id = bind.as_bytes(node_id, allow_none = False)

    ####################################################################################################################

    result_mac = (bind.c_uint8 * 6)()

    bind.lib.nyx_generate_mac_addr(
        result_mac,
        mac0 & 0xFF,
        mac1 & 0xFF,
        node_id
    )

    return bytes(result_mac)

